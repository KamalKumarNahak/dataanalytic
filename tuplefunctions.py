def tuple_methods_examples():
    print("\n1. count(): Returns the number of times an element appears in the tuple.")
    tpl = (1, 2, 3, 2, 1, 2)
    print(tpl.count(2))
    
    print("\n2. index(): Returns the index of the first occurrence of an element.")
    tpl = (1, 2, 3, 4, 5)
    print(tpl.index(3))
    
tuple_methods_examples()
